Color getColorByType(int type) {
  List<Color> resultColors = const [
    Color(0xFF5AAFDC),
    Color(0xFFAA96BE),
    Color(0xFFDC8778),
    Color(0xFFDB9B54),
    Color(0xFF6EB4B4),
    Color(0xFF96BE58),
    Color(0xFFD787AD),
    Color(0xFF55AA73),
    Color(0xFFE57887),
    Color(0xFF6969A5),
  ];

  // Ensure the type is within the valid range (1 to 10)
  if (type >= 1 && type <= 10) {
    return resultColors[type - 1];
  } else {
    // Default color if type is out of range
    return Colors.black;
  }
}
